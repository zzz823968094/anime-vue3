export const useI18nStore = defineStore('i18n', () => {
  const lang = ref(localStorage.getItem('site_lang') || 'zh_tw')

  function setLang(l) {
    lang.value = l
    localStorage.setItem('site_lang', l)
  }

  const t = computed(() => messages[lang.value] || messages['zh_tw'])

  return { lang, setLang, t }
})
